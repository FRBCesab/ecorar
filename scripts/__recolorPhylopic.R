recolorPhylopic <- function(img, alpha = 0.2, color = NULL) {

  if (is.null(color)) {

    mat <- matrix(
      rgb(
        img[ , , 1],
        img[ , , 2],
        img[ , , 3],
        img[ , , 4] * alpha
      ),
      nrow = dim(img)[1]
    )

  } else {

    cols   <- grDevices::col2rgb(color)
    imglen <- length(img[ , , 1])
    mat    <- matrix(
      ifelse(
        img[ , , 4] > 0,
        rgb(
          rep(cols[1, 1], imglen),
          rep(cols[2, 1], imglen),
          rep(cols[3, 1], imglen),
          img[ , , 4] * 255 * alpha,
          maxColorValue = 255
        ),
        rgb(
          rep(1, imglen),
          rep(1, imglen),
          rep(1, imglen),
          img[ , , 4] * alpha
        )
      ),
      nrow = dim(img)[1]
    )
  }

  return(mat)
}
